#operasi aritmatika

a = 10
b = 3

# Tambah
hasil = a + b
print (a,'+',b,'=',hasil)

#kurang
hasil = a - b
print (a,'-',b,'=',hasil)

#kali
hasil = a * b
print (a,'*',b,'=',hasil)

#bagi
hasil = a / b
print (a,'/',b,'=',hasil)

#operasi Eksponen(Pangkat)
hasil = a ** b
print (a,'**',b,'=',hasil)

#operasi Modulus(sisa bagi)
hasil = a % b
print (a,'%',b,'=',hasil)

#operasi Floor Division(Pembulatan)
hasil = a // b
print (a,'**',b,'=',hasil)